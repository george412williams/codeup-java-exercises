public class StringExercise {
    public static void main(String[] args){
        String rock = "We don't need no education\nWe don't need no thought control";
        System.out.println(rock);

        String bumpy = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(bumpy);

        String winDrive = "In windows, the main drive is usually C:\\";
        System.out.println(winDrive);

        String backslash = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backslash);
    }
}
